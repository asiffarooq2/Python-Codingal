# Different Data Types in Python

name = "Asif"          # String
age = 25               # Integer
height = 5.8           # Float
is_student = True      # Boolean
fruits = ["Apple", "Banana", "Mango"]  # List

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student:", is_student)
print("Fruits:", fruits)

# Display data types
print(type(name))
print(type(age))
print(type(height))
print(type(is_student))
print(type(fruits))
