# String Operations

text = "Hello Python"

print("Original String:", text)

# Length of string
print("Length:", len(text))

# Uppercase
print("Uppercase:", text.upper())

# Lowercase
print("Lowercase:", text.lower())

# Replace
print("Replace:", text.replace("Python", "World"))

# Find
print("Find 'Python':", text.find("Python"))

# Slicing
print("First 5 characters:", text[:5])

# Concatenation
new_text = text + " Programming"
print("Concatenation:", new_text)

# Repetition
print("Repeat:", text * 2)
